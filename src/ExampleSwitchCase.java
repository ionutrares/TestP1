public class ExampleSwitchCase {

    public static void maine(String[] args){

        int j = 5;

        switch(j){

            case 0:
                System.out.println("Value is 0");
                break;
            case 1:
                System.out.println("Values is 1");
                break;
            case 2:
                System.out.println("Values is 2");
                break;
            case 3:
                System.out.println("Values is 3");
                break;
            default:
                System.out.println("No Value");
                break;

        }
    }
}
