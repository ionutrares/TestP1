public class Exampleif {
    public static void main(String[] args) {

        int x = 20;
        int y = 20;

        if(x < y){
            System.out.printf("X is less than Y");
        } else if (x == y){
                System.out.printf("X is equal to Y");
            } else{
                System.out.printf("X is greater than Y");
            }
    }
}

